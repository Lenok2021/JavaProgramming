package day19_LoopsPractice;

public class LoopWithoutCurlyBraces {
    public static void main(String[] args) {

        for (int i = 0;i<5; i++)
            System.out.println("Hey"); // only hey will be printed
        //ONLY WHEN WE HAVE 1 STATEMENT {} ARE NOT MANDATORY!!!!!!!!!
        System.out.println("Hello"); // only hey will be printed


        for (int i = 0;i<5; i++){
            System.out.println("Hey");
            System.out.println("Hello");
        }






    }

}
