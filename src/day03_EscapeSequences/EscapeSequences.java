package day03_EscapeSequences;
/*
EscapeSequences MUST be  given with double quote
1) \n - starts a new line (backward slash)
2) \t - starts  a new paragraph space
3) \\ - double back slash gives you a sindle back slash (if you want to print one back slash you need to give to back slashes
4) \" - double quote.  if you want to print "  as symbol  you should use  \"




 */
public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Java \nPython \nC#");

        System.out.println("____________________");
        System.out.println("Hello Cydeo \nHow are you all today? \nIt's nice to see all today. \nWhat class do we have today");

        System.out.println("----------------");
        System.out.println("\tJava is Cool Programming Language");
        System.out.println("/\\");
        System.out.println("My favorite TV show is \"Game of Throne\"");



    }


}
