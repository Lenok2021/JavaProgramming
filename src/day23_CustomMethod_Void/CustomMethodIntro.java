package day23_CustomMethod_Void;

public class CustomMethodIntro {
    public static void main(String[] args) {

        System.out.println("Test started");
        greeting(); //  when we call  the name method  we will get execution
        System.out.println("Test completed");

    }
    public static void greeting(){
        System.out.println("Hello word");
        System.out.println("How are you today");
    }



    //  how execute method?  we need  main  method.  main method  are  used for making code runnable
    //  one  method MUST  BE independed
    // methods  we can create within any way, but not inside the another method
    // when  you  finish  the method click on minimize bottom,  once  the  method done
    //  does not  metter.  you can create the  method  before or after the main method
    //  we must  put  code fragment within  method,  otherwise  the  method does not have any
    // task  that  can be performed.
}
